#######################匯入模組#######################
# 匯入 thinter模組和random模組
from tkinter import *

#######################建立視窗########################
# 創建主視窗
windows = Tk()
#######################建立畫布########################
# 創建一個畫布，設定其寬度為600，高度為600
canvas = Canvas(windows, width=600, height=600, bg="white")
# 將畫布加入主視窗中
canvas.pack()
# 設定主視窗標題
windows.title("My first GUI")

#######################運行應用程式########################
# 開始執行主迴圈，等待用戶操作
windows.mainloop()
